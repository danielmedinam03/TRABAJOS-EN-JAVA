    private void jmiModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiModificarActionPerformed
        // TODO add your handling code here:
        lstEmpleados.setModificarData(
        jTable1.getModel().   //Código Empleado
            getValueAt(jTable1.getSelectedRow(), 0).toString(),
        jTable1.getModel().   //Nombres Empleado
            getValueAt(jTable1.getSelectedRow(), 1).toString(),
        Double.parseDouble(jTable1.getModel().  //# Horas T
            getValueAt(jTable1.getSelectedRow(), 2).toString()),
        Double.parseDouble(jTable1.getModel(). //Valor HT 
            getValueAt(jTable1.getSelectedRow(), 3).toString()),
        Double.parseDouble(jTable1.getModel().  //# Horas E
            getValueAt(jTable1.getSelectedRow(), 4).toString())
        );
        lstEmpleados.setLlenarJTable(jTable1);
    }//GEN-LAST:event_jmiModificarActionPerformed

